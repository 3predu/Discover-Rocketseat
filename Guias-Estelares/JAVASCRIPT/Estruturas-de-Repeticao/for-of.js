// for...of
// declaração que cria um loop através de alguma variável

let name = 'Predu'
let names = ['Mayk', 'Paulo', 'Joao']

// pegando as letras da variável 'name'

for(let char of names){
    console.log(char)
}

// pegando as letras de cada elemento do Array 'names'

for(let elements of names){
    console.log(elements)
}

