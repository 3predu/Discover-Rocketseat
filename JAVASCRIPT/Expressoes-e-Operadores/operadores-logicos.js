/*
    Operadores Lógicos (Logical Operators)

    - 2 valores booleanos, quando verificados, resultará
    em verdadeiro ou falso
*/

let pao = true
let queijo = true

// AND &&
console.log(pao && queijo)
// true && true == true
// true && false == false
// false && false == false

// OR ||
console.log(pao || queijo)
// true || true == true
// true || false == true
// false || false == false

// NOT !
console.log(!pao) // true
console.log(!queijo) // true